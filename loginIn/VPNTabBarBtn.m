#import "VPNTabBarBtn.h"

@implementation VPNTabBarBtn

-(CGRect)imageRectForContentRect:(CGRect)contentRect{
    return CGRectMake((self.frame.size.width - 30)/2, 0, 30, self.frame.size.height *0.6);
}

-(CGRect)titleRectForContentRect:(CGRect)contentRect{
    return CGRectMake(0, self.frame.size.height * 0.6, self.frame.size.width, self.frame.size.height - self.frame.size.height *0.6);
}


@end
